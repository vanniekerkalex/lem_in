#include "libft.h"

void	ft_putnbr(int n)
{
	char *s;

	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	s = ft_itoa(n);
	ft_putstr(s);
	free(s);
}
